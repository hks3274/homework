package h7_0308;

public abstract class Bonsa {
	private String storeName;
	private String gimchijjigae;
	private	String budaejjigae;
	private String bibimbap;
	private String Sundaeguk;
	private String rice;
	
	public abstract void menupan();

	public String getGimchijjigae() {
		return gimchijjigae;
	}

	public void setGimchijjigae(String gimchijjigae) {
		this.gimchijjigae = gimchijjigae;
	}

	public String getBudaejjigae() {
		return budaejjigae;
	}

	public void setBudaejjigae(String budaejjigae) {
		this.budaejjigae = budaejjigae;
	}

	public String getBibimbap() {
		return bibimbap;
	}

	public void setBibimbap(String bibimbap) {
		this.bibimbap = bibimbap;
	}

	public String getSundaeguk() {
		return Sundaeguk;
	}

	public void setSundaeguk(String sundaeguk) {
		Sundaeguk = sundaeguk;
	}

	public String getRice() {
		return rice;
	}

	public void setRice(String rice) {
		this.rice = rice;
	}

	public String getStoreName() {
		return storeName;
	}

	public void setStoreName(String storeName) {
		this.storeName = storeName;
	}

	
	
}
