package h6_0307;

public class Printer2 {

	static public void printerln(int i) {
		System.out.println(i);
	}
	
	static public void printerln(boolean b) {
		System.out.println(b);
	}

	static public void printerln(double d) {
		System.out.println(d);
	}

	static void printerln(String string) {
		System.out.println(string);
	}
	
}
