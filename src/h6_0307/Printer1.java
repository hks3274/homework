package h6_0307;

public class Printer1 {

	public void printerln(int i) {
		System.out.println(i);
	}
	
	public void printerln(boolean b) {
		System.out.println(b);
	}

	public void printerln(double d) {
		System.out.println(d);
	}

	public void printerln(String string) {
		System.out.println(string);
	}
	
}
